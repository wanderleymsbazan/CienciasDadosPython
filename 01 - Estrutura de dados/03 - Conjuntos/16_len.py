# Utilizando a função len() para obter o tamanho de um conjunto.

# Inicializando um conjunto chamado "numeros".
numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

# Usando a função len() para obter o tamanho do conjunto.
tamanho = len(numeros)

# Exibindo o tamanho do conjunto.
print(tamanho)  # Resultado: 10

#     O conjunto "numeros" é inicializado com vários elementos, incluindo duplicatas.

#     A função len() é utilizada para obter o tamanho do conjunto, ou seja, o número de elementos únicos presentes nele.

#     O valor retornado pela função len() é armazenado na variável tamanho.

#     O tamanho do conjunto é exibido usando print(tamanho).

# O resultado será o número 10, indicando que há 10 elementos únicos no conjunto "numeros". Duplicatas são automaticamente removidas em um conjunto, e apenas os elementos únicos são mantidos.