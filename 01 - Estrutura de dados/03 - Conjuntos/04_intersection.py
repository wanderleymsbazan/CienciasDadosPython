# Interseção de conjuntos usando o método intersection().

# Dois conjuntos iniciais.
conjunto_a = {1, 2, 3}
conjunto_b = {2, 3, 4}

# Usando o método intersection() para criar a interseção dos conjuntos.
resultado = conjunto_a.intersection(conjunto_b)

# Exibindo o resultado da interseção.
print(resultado)  # Resultado: {2, 3}

#     Você começa definindo dois conjuntos conjunto_a e conjunto_b, que contêm os elementos 1, 2, 3 e 2, 3, 4, respectivamente.

#     O método intersection() é usado para criar a interseção dos dois conjuntos. Ele retorna um novo conjunto contendo os elementos que estão presentes em ambos os conjuntos originais.

#     O resultado da interseção é armazenado na variável resultado.

#     O print(resultado) exibe o conjunto resultante, que contém os elementos {2, 3}, que são os elementos presentes em ambos os conjuntos.

# O método intersection() é útil para encontrar os elementos que são comuns entre dois conjuntos.
