# União de conjuntos usando o método union().

# Dois conjuntos iniciais.
conjunto_a = {1, 2}
conjunto_b = {3, 4}

# Usando o método union() para criar a união dos conjuntos.
resultado = conjunto_a.union(conjunto_b)

# Exibindo o resultado da união.
print(resultado)  # Resultado: {1, 2, 3, 4}

#     Você começa definindo dois conjuntos conjunto_a e conjunto_b que contêm os elementos 1, 2 e 3, 4, respectivamente.

#     O método union() é usado para criar a união dos dois conjuntos. Ele retorna um novo conjunto contendo todos os elementos únicos presentes em ambos os conjuntos originais.

#     O resultado da união é armazenado na variável resultado.

#     O print(resultado) exibe o conjunto resultante, que contém os elementos {1, 2, 3, 4}.

# O método union() é uma maneira conveniente de criar um novo conjunto que contém todos os elementos únicos dos conjuntos originais.