# Remoção de todos os elementos de um conjunto usando o método clear().

# Inicializando um conjunto chamado "sorteio".
sorteio = {1, 23}

# Exibindo o conjunto antes da remoção.
print(sorteio)  # Resultado: {1, 23}

# Usando o método clear() para remover todos os elementos do conjunto "sorteio".
sorteio.clear()

# Exibindo o conjunto após a remoção.
print(sorteio)  # Resultado: set()

#     Você começa inicializando um conjunto chamado "sorteio" com os elementos 1 e 23.

#     O método clear() é usado para remover todos os elementos de um conjunto, deixando-o vazio. Quando você chama sorteio.clear(), todos os elementos presentes no conjunto "sorteio" são removidos.

#     O conjunto "sorteio" está vazio após a chamada do método clear(), e isso é confirmado pelo print(sorteio).

# O método clear() é útil quando você deseja remover todos os elementos de um conjunto, deixando-o vazio para ser preenchido posteriormente com novos elementos.