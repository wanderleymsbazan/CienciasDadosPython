# Convertendo um conjunto para uma lista e acessando o primeiro elemento.

# Criando um conjunto de números.
numeros = {1, 2, 3, 2}

# Convertendo o conjunto para uma lista.
numeros = list(numeros)

# Acessando o primeiro elemento da lista (índice 0).
print(numeros[0])  # Resultado: 1

#     Um conjunto numeros é criado com os elementos {1, 2, 3, 2}. Note que o número 2 aparece duas vezes, mas os conjuntos não permitem elementos duplicados, então apenas uma instância do número 2 é mantida no conjunto.

#     A linha numeros = list(numeros) converte o conjunto numeros para uma lista, resultando na lista [1, 2, 3].

#     Finalmente, a linha print(numeros[0]) imprime o primeiro elemento da lista (índice 0), que é 1.

# Dessa forma, você converteu o conjunto para uma lista e acessou o primeiro elemento da lista usando a notação de índice.