numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

print(1 in numeros)  # True
print(10 in numeros)  # False

    # numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}: Aqui, um conjunto chamado "numeros" é criado, contendo vários números inteiros. Lembre-se de que conjuntos não permitem duplicatas, então mesmo que haja duplicatas na inicialização, o conjunto só conterá um único valor de cada.

    # print(1 in numeros) # True: Esta linha verifica se o número 1 está presente no conjunto "numeros". O operador in é usado para verificar a pertinência de um elemento a um conjunto. Como o número 1 está presente no conjunto, a expressão retorna True, o que significa que o número 1 faz parte do conjunto.

    # print(10 in numeros) # False: Aqui, verifica-se se o número 10 está presente no conjunto "numeros". Como o número 10 não está no conjunto, a expressão retorna False, indicando que o número 10 não faz parte do conjunto.