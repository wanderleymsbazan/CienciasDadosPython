# Demonstração do método pop() em uma lista.

# Lista de linguagens.
linguagens = ["python", "js", "c", "java", "csharp"]

# Uso do método pop() para remover e retornar o último elemento da lista.
print(linguagens.pop())  # Resultado: "csharp"
print(linguagens.pop())  # Resultado: "java"
print(linguagens.pop())  # Resultado: "c"
print(linguagens.pop(0))  # Resultado: "python"

#     A lista linguagens contém cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha print(linguagens.pop()) utiliza o método pop() para remover e retornar o último elemento da lista, resultando em "csharp".

#     A linha subsequente print(linguagens.pop()) utiliza novamente o método pop() para remover e retornar o novo último elemento da lista, resultando em "java".

#     A próxima linha print(linguagens.pop()) continua utilizando o método pop() para remover e retornar o último elemento, resultando em "c".

#     Finalmente, a linha print(linguagens.pop(0)) utiliza o método pop(0) para remover e retornar o elemento no índice 0 da lista (primeiro elemento), resultando em "python".

