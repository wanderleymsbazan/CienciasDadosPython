# Demonstração do método clear() em uma lista.

# Criação da lista com elementos diversos.
lista = [1, "Python", [40, 30, 20]]

# Impressão da lista original.
print(lista)  # Resultado: [1, "Python", [40, 30, 20]]

# Uso do método clear() para remover todos os elementos da lista.
lista.clear()

# Impressão da lista após o método clear().
print(lista)  # Resultado: []

#     A lista lista é criada inicialmente com três elementos: o número 1, a string "Python" e uma lista [40, 30, 20].

#     A linha print(lista) imprime a lista original, resultando em [1, "Python", [40, 30, 20]].

#     O método clear() é usado para remover todos os elementos da lista, deixando-a vazia.

#     A linha print(lista) após o método clear() imprime a lista vazia, resultando em [].

