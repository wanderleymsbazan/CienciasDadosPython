# Demonstração da função len() em uma lista.

# Lista de linguagens.
linguagens = ["python", "js", "c", "java", "csharp"]

# Uso da função len() para obter o tamanho (número de elementos) da lista.
print(len(linguagens))  # Resultado: 5

#     A lista linguagens é criada com cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha print(len(linguagens)) utiliza a função len() para obter o tamanho (número de elementos) da lista. O resultado é 5, pois a lista contém cinco elementos.

