# Demonstração do método index() em uma lista.

# Lista de linguagens.
linguagens = ["python", "js", "c", "java", "csharp"]

# Uso do método index() para encontrar o índice de um elemento na lista.
print(linguagens.index("java"))  # Resultado: 3
print(linguagens.index("python"))  # Resultado: 0

#     A lista linguagens contém cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha linguagens.index("java") utiliza o método index() para encontrar o índice do elemento "java" na lista, resultando em 3.

#     A linha linguagens.index("python") utiliza o método index() para encontrar o índice do elemento "python" na lista, resultando em 0.

# O código demonstra como usar o método index() para encontrar o índice de um elemento específico em uma lista em Python.
