# Acesso aos elementos de uma lista usando índices negativos.

# Criação da lista de frutas.
frutas = ["maçã", "laranja", "uva", "pera"]

# Acesso ao último elemento da lista usando índice negativo -1 e impressão.
print(frutas[-1])  # Resultado: pera

# Acesso ao terceiro elemento a partir do final da lista usando índice negativo -3 e impressão.
print(frutas[-3])  # Resultado: laranja

#     A lista frutas é criada com quatro elementos: "maçã", "laranja", "uva" e "pera".

#     A linha print(frutas[-1]) acessa o último elemento da lista usando índice negativo -1 e imprime "pera".

#     A linha print(frutas[-3]) acessa o terceiro elemento a partir do final da lista usando índice negativo -3 e imprime "laranja".

# O código demonstra como acessar elementos específicos de uma lista usando índices negativos e imprimir o valor correspondente.