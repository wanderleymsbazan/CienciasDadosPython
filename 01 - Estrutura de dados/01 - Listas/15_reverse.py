# Demonstração do método reverse() em uma lista.

# Lista de linguagens.
linguagens = ["python", "js", "c", "java", "csharp"]

# Uso do método reverse() para inverter a ordem dos elementos na lista.
linguagens.reverse()

# Impressão da lista após a inversão.
print(linguagens)  # Resultado: ["csharp", "java", "c", "js", "python"]

#     A lista linguagens contém cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha linguagens.reverse() utiliza o método reverse() para inverter a ordem dos elementos na lista. Após essa linha, os elementos estarão na ordem inversa.

#     A linha print(linguagens) imprime a lista após a inversão, resultando em ["csharp", "java", "c", "js", "python"].

