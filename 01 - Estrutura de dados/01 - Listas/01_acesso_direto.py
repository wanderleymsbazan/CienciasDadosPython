# Acesso aos elementos de uma lista usando índices.

# Criação da lista de frutas.
frutas = ["maçã", "laranja", "uva", "pera"]

# Acesso ao primeiro elemento da lista (índice 0) e impressão.
print(frutas[0])  # Resultado: maçã

# Acesso ao terceiro elemento da lista (índice 2) e impressão.
print(frutas[2])  # Resultado: uva

#     A lista frutas é criada com quatro elementos: "maçã", "laranja", "uva" e "pera".

#     A linha print(frutas[0]) acessa o primeiro elemento da lista (índice 0) e imprime "maçã".

#     A linha print(frutas[2]) acessa o terceiro elemento da lista (índice 2) e imprime "uva".

# O código demonstra como acessar elementos específicos de uma lista usando índices e imprimir o valor correspondente.