# Demonstração do método count() em uma lista.

# Lista de cores.
cores = ["vermelho", "azul", "verde", "azul"]

# Uso do método count() para contar o número de ocorrências de elementos na lista.
print(cores.count("vermelho"))  # Resultado: 1
print(cores.count("azul"))  # Resultado: 2
print(cores.count("verde"))  # Resultado: 1

#     A lista cores é criada com quatro elementos: "vermelho", "azul", "verde" e "azul".

#     A linha cores.count("vermelho") utiliza o método count() para contar quantas vezes o elemento "vermelho" aparece na lista, resultando em 1.

#     A linha cores.count("azul") utiliza o método count() para contar quantas vezes o elemento "azul" aparece na lista, resultando em 2.

#     A linha cores.count("verde") utiliza o método count() para contar quantas vezes o elemento "verde" aparece na lista, resultando em 1.

# O código demonstra como usar o método count() para contar o número de ocorrências de elementos específicos em uma lista.