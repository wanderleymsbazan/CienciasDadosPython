# Demonstração de uso do método append() em uma lista.

# Criação de uma lista vazia.
lista = []

# Adição de elementos à lista usando o método append().
lista.append(1)  # Adiciona o número 1 à lista.
lista.append("Python")  # Adiciona a string "Python" à lista.
lista.append([40, 30, 20])  # Adiciona uma lista [40, 30, 20] à lista principal.

# Impressão da lista completa.
print(lista)  # Resultado: [1, "Python", [40, 30, 20]]

#     Uma lista vazia é criada inicialmente.

#     O método append() é usado para adicionar elementos à lista.
#         lista.append(1) adiciona o número inteiro 1 à lista.
#         lista.append("Python") adiciona a string "Python" à lista.
#         lista.append([40, 30, 20]) adiciona uma lista [40, 30, 20] à lista principal.

#     A linha print(lista) imprime a lista completa, resultando em [1, "Python", [40, 30, 20]].

# O código demonstra como usar o método append() para adicionar elementos a uma lista em Python.