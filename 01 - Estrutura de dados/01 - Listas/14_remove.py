# Demonstração do método remove() em uma lista.

# Lista de linguagens.
linguagens = ["python", "js", "c", "java", "csharp"]

# Uso do método remove() para remover o elemento "c" da lista.
linguagens.remove("c")

# Impressão da lista após a remoção.
print(linguagens)  # Resultado: ["python", "js", "java", "csharp"]

#     A lista linguagens contém cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha linguagens.remove("c") utiliza o método remove() para remover o primeiro elemento "c" encontrado na lista. Nesse caso, ele remove o primeiro "c".

#     A linha print(linguagens) imprime a lista após a remoção, resultando em ["python", "js", "java", "csharp"].

