# Acessando elementos de uma tupla.

# Tupla de frutas.
frutas = ("maçã", "laranja", "uva", "pera",)

# Acessando o primeiro elemento da tupla (índice 0).
print(frutas[0])  # Resultado: "maçã"

# Acessando o terceiro elemento da tupla (índice 2).
print(frutas[2])  # Resultado: "uva"

#     A tupla frutas é criada com quatro elementos: "maçã", "laranja", "uva" e "pera".

#     A linha print(frutas[0]) imprime o primeiro elemento da tupla, que é "maçã".

#     A linha print(frutas[2]) imprime o terceiro elemento da tupla, que é "uva".

# O código demonstra como acessar elementos específicos de uma tupla usando índices. Lembre-se de que os índices em Python começam em 0, então o primeiro elemento tem índice 0, o segundo tem índice 1 e assim por diante.