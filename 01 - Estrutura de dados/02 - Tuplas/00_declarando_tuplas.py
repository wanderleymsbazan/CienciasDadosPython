# Demonstração da criação de tuplas.

# Tupla de frutas.
frutas = (
    "laranja",
    "pera",
    "uva",
)
print(frutas)  # Resultado: ("laranja", "pera", "uva")

# Tupla de letras.
letras = tuple("python")
print(letras)  # Resultado: ("p", "y", "t", "h", "o", "n")

# Tupla de números.
numeros = tuple([1, 2, 3, 4])
print(numeros)  # Resultado: (1, 2, 3, 4)

# Tupla de um único elemento.
pais = ("Brasil",)
print(pais)  # Resultado: ("Brasil",)

#     A tupla frutas é criada com três elementos: "laranja", "pera" e "uva".

#     A linha letras = tuple("python") cria uma tupla contendo cada caractere da string "python".

#     A linha numeros = tuple([1, 2, 3, 4]) cria uma tupla a partir de uma lista de números.

#     A tupla pais é criada com um único elemento: "Brasil". Note que uma tupla com um único elemento deve ter uma vírgula após o elemento.

# O código demonstra diferentes maneiras de criar tuplas em Python. As tuplas são estruturas de dados semelhantes a listas, mas são imutáveis, ou seja, seus elementos não podem ser modificados após a criação.