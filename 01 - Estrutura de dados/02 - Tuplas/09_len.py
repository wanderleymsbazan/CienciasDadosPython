# Usando a função len() para obter o tamanho de uma tupla.

# Tupla de linguagens.
linguagens = (
    "python",
    "js",
    "c",
    "java",
    "csharp",
)

# Obtendo o tamanho (número de elementos) da tupla.
print(len(linguagens))  # Resultado: 5

#     A tupla linguagens é criada com cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha len(linguagens) calcula e retorna o tamanho da tupla, que é o número total de elementos presentes na tupla. Nesse caso, o tamanho é 5.

# A função len() é usada para obter o tamanho (número de elementos) de qualquer sequência, incluindo tuplas.