# Usando o método index() para encontrar o índice de elementos em uma tupla.

# Tupla de linguagens.
linguagens = ("python", "js", "c", "java", "csharp",)

# Encontrando o índice do elemento "java" na tupla.
print(linguagens.index("java"))  # Resultado: 3

# Encontrando o índice do elemento "python" na tupla.
print(linguagens.index("python"))  # Resultado: 0

#     A tupla linguagens é criada com cinco elementos: "python", "js", "c", "java" e "csharp".

#     A linha linguagens.index("java") encontra o índice do elemento "java" na tupla, resultando em 3.

#     A linha linguagens.index("python") encontra o índice do elemento "python" na tupla, resultando em 0.

# O método index() é usado para obter o índice do primeiro elemento encontrado na tupla que corresponde ao valor especificado.