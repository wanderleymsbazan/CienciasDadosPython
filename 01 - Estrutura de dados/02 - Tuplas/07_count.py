# Usando o método count() para contar elementos em uma tupla.

# Tupla de cores.
cores = (
    "vermelho",
    "azul",
    "verde",
    "azul",
)

# Contando o número de ocorrências do elemento "vermelho" na tupla.
print(cores.count("vermelho"))  # Resultado: 1

# Contando o número de ocorrências do elemento "azul" na tupla.
print(cores.count("azul"))  # Resultado: 2

# Contando o número de ocorrências do elemento "verde" na tupla.
print(cores.count("verde"))  # Resultado: 1

#     A tupla cores é criada com quatro elementos: "vermelho", "azul", "verde" e "azul".

#     A linha cores.count("vermelho") conta quantas vezes o elemento "vermelho" aparece na tupla, resultando em 1 ocorrência.

#     A linha cores.count("azul") conta quantas vezes o elemento "azul" aparece na tupla, resultando em 2 ocorrências.

#     A linha cores.count("verde") conta quantas vezes o elemento "verde" aparece na tupla, resultando em 1 ocorrência.

# O método count() é usado para contar a quantidade de vezes que um elemento específico aparece na tupla.