# Acessando elementos de uma tupla usando índices negativos.

# Tupla de frutas.
frutas = (
    "maçã",
    "laranja",
    "uva",
    "pera",
)

# Acessando o último elemento da tupla usando índice negativo -1.
print(frutas[-1])  # Resultado: "pera"

# Acessando o terceiro elemento da tupla a partir do final usando índice negativo -3.
print(frutas[-3])  # Resultado: "laranja"

#     A tupla frutas é criada com quatro elementos: "maçã", "laranja", "uva" e "pera".

#     A linha print(frutas[-1]) imprime o último elemento da tupla, que é "pera", usando o índice negativo -1.

#     A linha print(frutas[-3]) imprime o terceiro elemento da tupla a partir do final, que é "laranja", usando o índice negativo -3.

# Índices negativos em Python permitem acessar elementos a partir do final da sequência. O índice -1 corresponde ao último elemento, o índice -2 ao penúltimo e assim por diante.