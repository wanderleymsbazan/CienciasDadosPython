# Demonstração de diferentes tipos de expressões e valores.

# Expressão de adição de inteiros.
print(11 + 10 + 1000)  # Resultado: 1021

# Expressão de adição de números de diferentes tipos (float e int).
print(1.5 + 1 + 0.5)  # Resultado: 3.0

# Valor booleano True.
print(True)  # Resultado: True

# Valor booleano False.
print(False)  # Resultado: False

# String "Python".
print("Python")  # Resultado: Python

#     A expressão 11 + 10 + 1000 resulta na soma dos três valores inteiros, resultando em 1021.

#     A expressão 1.5 + 1 + 0.5 resulta na soma dos três valores, incluindo um float e dois inteiros. O resultado é um float, que é 3.0.

#     O valor booleano True é uma constante booleana que representa a verdade.

#     O valor booleano False é uma constante booleana que representa a falsidade.

#     A string "Python" é uma sequência de caracteres, que é impressa como está.

