# Converte o número decimal 1.97348728 para um inteiro, removendo a parte decimal.
print(int(1.97348728))

# Converte a string "10" para um inteiro. A função int() interpreta a string como um número inteiro.
print(int("10"))

# Converte a string "10.10" para um número de ponto flutuante (float).
print(float("10.10"))

# Converte o número inteiro 100 para um número de ponto flutuante (float).
print(float(100))

# Atribui o valor inteiro 10 à variável "valor".
valor = 10

# Converte o valor da variável "valor" (que é um inteiro) para uma string.
valor_str = str(valor)

# Imprime o tipo de dado da variável "valor". Nesse caso, será <class 'int'>.
print(type(valor))

# Imprime o tipo de dado da variável "valor_str". Nesse caso, será <class 'str'>, pois foi convertida para string anteriormente.
print(type(valor_str))

# Realiza a divisão de 100 por 2. O resultado é um número de ponto flutuante.
print(100 / 2)

# Realiza a divisão de 100 por 2, mas usando a divisão de piso (floor division). O resultado é um número inteiro.
print(100 // 2)

#Esses códigos demonstram várias operações de conversão entre tipos de dados (inteiros e strings, inteiros e floats), além de ilustrar o uso de operações matemáticas simples, como a divisão e a divisão de piso. Também é mostrado como verificar o tipo de dados de uma variável usando a função type().