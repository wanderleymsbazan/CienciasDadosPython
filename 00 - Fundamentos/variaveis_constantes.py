# Atribuição de variáveis e uso de constantes.

# Atribuição de valores às variáveis nome e idade.
nome = "Wanderley"
idade = 41

# Atribuição de novos valores às variáveis nome e idade em uma única linha.
nome, idade = "Camilly", 17

# Impressão das variáveis nome e idade após a nova atribuição.
print(nome, idade)  # Resultado: Camilly 17

# Definição de uma constante para o limite de saque diário.
limite_saque_diario = 1000

# Definição de uma lista de estados brasileiros como constante.
BRAZILIAN_STATES = ["SP", "RJ", "SC", "RS"]

# Impressão da lista de estados brasileiros.
print(BRAZILIAN_STATES)  # Resultado: ['SP', 'RJ', 'SC', 'RS']

#     As variáveis nome e idade são atribuídas valores iniciais ("Wanderley" e 41).

#     Na linha nome, idade = "Camilly", 17, as variáveis nome e idade recebem novos valores ("Camilly" e 17) em uma única linha.

#     A função print(nome, idade) é usada para imprimir os valores atuais das variáveis nome e idade.

#     A variável limite_saque_diario é definida para armazenar o limite de saque diário, que é 1000.

#     A variável BRAZILIAN_STATES é definida como uma lista constante que contém alguns estados brasileiros.

#     A função print(BRAZILIAN_STATES) é usada para imprimir a lista de estados brasileiros.

# O código demonstra a atribuição de valores a variáveis, a reatribuição de valores em uma única linha, e a definição de constantes em Python.
