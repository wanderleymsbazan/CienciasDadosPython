saldo = 200  # Atribuição do valor 200 à variável saldo.
saque = 200  # Atribuição do valor 200 à variável saque.

# Verifica se o valor de saldo é igual ao valor de saque.
print(saldo == saque)

# Verifica se o valor de saldo é diferente do valor de saque.
print(saldo != saque)

# Verifica se o valor de saldo é maior do que o valor de saque.
print(saldo > saque)

# Verifica se o valor de saldo é maior ou igual ao valor de saque.
print(saldo >= saque)

# Verifica se o valor de saldo é menor do que o valor de saque.
print(saldo < saque)

# Verifica se o valor de saldo é menor ou igual ao valor de saque.
print(saldo <= saque)

#     A variável saldo é atribuída com o valor 200, e a variável saque também é atribuída com o valor 200.

#     A expressão saldo == saque verifica se o valor de saldo é igual ao valor de saque. Como ambos têm o valor 200, a expressão resulta em True.

#     A expressão saldo != saque verifica se o valor de saldo é diferente do valor de saque. Como ambos têm o valor 200, a expressão resulta em False.

#     A expressão saldo > saque verifica se o valor de saldo é maior do que o valor de saque. Como ambos têm o mesmo valor, a expressão resulta em False.

#     A expressão saldo >= saque verifica se o valor de saldo é maior ou igual ao valor de saque. Como ambos têm o mesmo valor, a expressão resulta em True.

#     A expressão saldo < saque verifica se o valor de saldo é menor do que o valor de saque. Como ambos têm o mesmo valor, a expressão resulta em False.

#     A expressão saldo <= saque verifica se o valor de saldo é menor ou igual ao valor de saque. Como ambos têm o mesmo valor, a expressão resulta em True.

# O código demonstra diferentes operações de comparação entre as variáveis saldo e saque usando operadores de comparação (==, !=, >, >=, <, <=) em Python.