saldo = 500  # Atribuição inicial do valor 500 à variável saldo.
print(saldo)  # Impressão do valor atual de saldo.

saldo = 200  # Atribuição de um novo valor (200) à variável saldo.
print(saldo)  # Impressão do novo valor de saldo.

saldo += 10  # Incremento do valor de saldo por 10.
print(saldo)  # Impressão do valor após o incremento.

saldo -= 5  # Decremento do valor de saldo por 5.
print(saldo)  # Impressão do valor após o decremento.

saldo //= 2  # Divisão de piso do valor de saldo por 2.
print(saldo)  # Impressão do valor após a divisão de piso.

saldo /= 2  # Divisão do valor de saldo por 2.
print(saldo)  # Impressão do valor após a divisão.

saldo *= 10  # Multiplicação do valor de saldo por 10.
print(saldo)  # Impressão do valor após a multiplicação.

saldo %= 4  # Cálculo do resto da divisão do valor de saldo por 4.
print(saldo)  # Impressão do valor do resto da divisão.

saldo **= 2  # Elevação do valor de saldo ao quadrado.
print(saldo)  # Impressão do valor após a elevação ao quadrado.

#     A variável saldo é inicializada com o valor 500, e esse valor é impresso na tela.

#     O valor da variável saldo é atualizado para 200, substituindo o valor anterior, e esse novo valor é impresso.

#     O operador += é usado para incrementar o valor da variável saldo em 10. O valor resultante (210) é impresso.

#     O operador -= é usado para decrementar o valor da variável saldo em 5. O valor resultante (205) é impresso.

#     O operador //= é usado para fazer uma divisão de piso do valor da variável saldo por 2. O resultado (102) é impresso.

#     O operador /= é usado para dividir o valor da variável saldo por 2. O resultado (51.0) é impresso.

#     O operador *= é usado para multiplicar o valor da variável saldo por 10. O resultado (510.0) é impresso.

#     O operador %= é usado para calcular o resto da divisão do valor da variável saldo por 4. O resultado (2.0) é impresso.

#     O operador **= é usado para elevar o valor da variável saldo ao quadrado. O resultado (4.0) é impresso.

