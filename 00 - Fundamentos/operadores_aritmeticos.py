produto_1 = 20
produto_2 = 10

# Soma dos valores de produto_1 e produto_2.
print(produto_1 + produto_2)

# Subtração dos valores de produto_1 e produto_2.
print(produto_1 - produto_2)

# Divisão dos valores de produto_1 por produto_2.
print(produto_1 / produto_2)

# Divisão de piso dos valores de produto_1 por produto_2.
print(produto_1 // produto_2)

# Multiplicação dos valores de produto_1 e produto_2.
print(produto_1 * produto_2)

# Resto da divisão de produto_1 por produto_2.
print(produto_1 % produto_2)

# Produto_1 elevado à potência de produto_2.
print(produto_1 ** produto_2)

x = (10 + 5) * 4
y = (10 / 2) + 25 * ((2 - 2) ** 2)

# Valor de x após a avaliação da expressão (10 + 5) * 4.
print(x)

# Valor de y após a avaliação da expressão (10 / 2) + 25 * ((2 - 2) ** 2).
print(y)

# Aqui está uma explicação detalhada de cada parte do código:

#     As variáveis produto_1 e produto_2 recebem os valores 20 e 10, respectivamente.

#     As operações matemáticas básicas são realizadas entre produto_1 e produto_2, e o resultado de cada operação é impresso na tela.

#     A variável x recebe o resultado da avaliação da expressão (10 + 5) * 4, que é 60.

#     A variável y recebe o resultado da avaliação da expressão (10 / 2) + 25 * ((2 - 2) ** 2). Aqui, os parênteses internos são avaliados primeiro (2 - 2 é 0), depois a expressão é avaliada como 25 * 0, resultando em 0. A expressão final se torna (10 / 2) + 0, que é 5.

# O código demonstra diferentes operações matemáticas e a ordem de avaliação de expressões em Python.