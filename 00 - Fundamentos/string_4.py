# Uso de strings formatadas e strings de múltiplas linhas.

nome = "Wanderley"

# Criação de uma mensagem com formatação usando f-string.
mensagem = f"""
   Olá meu nome é {nome},
 Eu estou aprendendo Python.
     Essa mensagem tem diferentes recuos.
"""

# Impressão da mensagem formatada.
print(mensagem)

# Impressão de um menu usando uma string de múltiplas linhas.
print(
    """
    ============= MENU =============

    1 - Depositar
    2 - Sacar
    0 - Sair

    ================================

            Obrigado por usar nosso sistema!!!!
"""
)
#     A variável nome contém o valor "Wanderley".

#     A variável mensagem é criada usando uma f-string (format string), que permite a inserção de variáveis e expressões dentro da string delimitada por aspas triplas ("""). Isso permite a formatação e inclusão de variáveis de maneira mais conveniente.

#     A string mensagem contém um texto com recuos diferentes para criar uma formatação visual agradável quando a mensagem é impressa.

#     A função print(mensagem) imprime a mensagem formatada com a formatação e variáveis substituídas.

#     A segunda parte do código imprime um menu formatado usando uma string de múltiplas linhas. As aspas triplas permitem definir a string em várias linhas, o que facilita a criação de um layout mais organizado.

#     A função print() é usada para imprimir a string do menu.

# O código demonstra como criar e formatar strings usando f-strings e como criar mensagens formatadas e layouts organizados usando strings de múltiplas linhas.
